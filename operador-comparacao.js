var numero = 5;

if(numero === 5) {
    console.log("O numero é 5");
}
else if (numero !== 5) {
    console.log("O numero não é 5");
}

var numer = '5';

if(numer !== 5) {
    console.log("Não é o numero 5 do tipo number");
}

// ===, !== : reforça pra ver se é o numero mesmo indicado e o tipo de dado